    public HashMap<String, String> getOltVal(String board, String sfp, String olt) throws IOException {
        HashMap<String, String> hash = new HashMap<String, String>();
        StringBuilder sb = new StringBuilder();
        File fr = new File("oltList/").getCanonicalFile();
        if (!fr.exists()) {
            fr.mkdir();
        }
        File[] ar = fr.listFiles();
        for (int i = 0; i < ar.length; i++) {
            if (ar[i].getName().equalsIgnoreCase(olt)) {
                Path path = Paths.get(ar[i].getAbsolutePath());
                Scanner scanner = new Scanner(path);
                //читаем построчно
                while (scanner.hasNextLine()) {
                    String line = scanner.nextLine();
                    sb.append(line);
                    sb.append("\n");
                }
            }
        }
        String[] s = sb.toString().split("\n");
        hash.put("ip", s[1]);
        hash.put("log", s[2]);
        hash.put("pass", s[3]);
        int a = s.length - 4;
        int b = 3;
        for (int i = 0; i <= a; i++) {
            String[] arr = s[b].split(" ");
            if (arr[0].equals(board)) {
                hash.put("plata", arr[0]);
                hash.put("type", arr[1]);
                hash.put("line", arr[2]);
                hash.put("SVLAN", arr[3]);
                break;
            }
            b++;
        }
        return hash;
    }

